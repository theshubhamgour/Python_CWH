# list methds:
# 1. Sorting
l1 = [1, 8, 7, 2, 21, 15]
print(l1.sort())
# will get a output as none

print(l1)
# now got the actual values

# 2.Reverse
l1.reverse()
print(l1)

# 3.Append - at the end of the list
l1.append(56)
print(l1)

# 4. insert: 
l1.insert(3, 8)
print(l1)

# 5. Pop - will delete element from the particulae index
l1.pop(0)
print(l1)

# 6. remove
l1.remove(21)
print(l1)